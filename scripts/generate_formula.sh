#!/usr/bin/env bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
cd "$REPO_ROOT"

REPO="MayaFlux/MayaFlux"

API_URL="https://api.github.com/repos/$REPO/releases"
AUTH_HEADER=""
if [[ -n "${GITHUB_TOKEN:-}" ]]; then
    AUTH_HEADER="-H 'Authorization: token $GITHUB_TOKEN'"
fi

echo "Fetching latest release from GitHub..."
if [[ -n "$AUTH_HEADER" ]]; then
    RELEASE_JSON=$(eval curl -s -H "Accept: application/vnd.github.v3+json" "$AUTH_HEADER" "\"$API_URL\"")
else
    RELEASE_JSON=$(curl -s -H "Accept: application/vnd.github.v3+json" "$API_URL")
fi

if [[ -z "$RELEASE_JSON" ]] || [[ "$RELEASE_JSON" == "[]" ]]; then
    echo "ERROR: Could not fetch releases"
    exit 1
fi

TAG=$(echo "$RELEASE_JSON" | grep -m1 '"tag_name"' | sed 's/.*"tag_name":\s*//' | sed 's/[",]//g' | xargs)
if [ -z "$TAG" ]; then
    echo "ERROR: Could not extract tag from release data"
    exit 1
fi

echo "Latest tag: $TAG"

ASSET_URL=$(python3 -c "
import json, sys
data = json.loads(sys.stdin.read())
if isinstance(data, list):
    data = data[0]
for asset in data.get('assets', []):
    url = asset.get('browser_download_url', '')
    if 'macos-arm64' in url and url.endswith('.tar.gz'):
        print(url)
        break
" <<<"$RELEASE_JSON")

if [ -z "$ASSET_URL" ]; then
    echo "WARNING: No direct asset URL found, constructing..."
    CLEAN_TAG=${TAG#v}

    PATTERNS=(
        "MayaFlux-${CLEAN_TAG}-macos-arm64.tar.gz"
        "MayaFlux-${TAG}-macos-arm64.tar.gz"
    )

    for PATTERN in "${PATTERNS[@]}"; do
        TEST_URL="https://github.com/$REPO/releases/download/${TAG}/${PATTERN}"
        echo "Trying: $TEST_URL"
        if curl -s -I "$TEST_URL" 2>/dev/null | head -1 | grep -q "200"; then
            ASSET_URL="$TEST_URL"
            break
        fi
    done
fi

if [ -z "$ASSET_URL" ]; then
    echo "ERROR: Could not find download URL"
    exit 1
fi

echo "Download URL: $ASSET_URL"

TEMP_FILE=$(mktemp)
echo "Downloading to calculate SHA256..."
curl -fL --progress-bar "$ASSET_URL" -o "$TEMP_FILE"

if [[ ! -s "$TEMP_FILE" ]]; then
    echo "ERROR: Download failed or empty file"
    rm -f "$TEMP_FILE"
    exit 1
fi

SHA256=$(shasum -a 256 "$TEMP_FILE" | awk '{print $1}')
rm -f "$TEMP_FILE"

echo "SHA256: $SHA256"
VERSION=${TAG#v}

mkdir -p Formula

cat >Formula/mayaflux-dev.rb <<RUBY
